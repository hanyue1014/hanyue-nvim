-- qol vim options

-- use system clipboard
vim.o.clipboard = "unnamedplus"

-- enable line numbers
vim.wo.number = true
vim.wo.rnu = true

-- save undo history
vim.o.undofile = true

-- enable break indent
vim.o.breakindent = true

-- indent with tabs and each indent is 4 chars long
vim.o.expandtab = true
vim.o.smartindent = true
vim.o.tabstop = 4
vim.o.shiftwidth = 4
